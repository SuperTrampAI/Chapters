import turtle
# 线条的粗细
turtle.pensize(8)
# 线条的颜色
turtle.pencolor('yellow')

# 线条的长度
turtle.forward(200)
# 角度
turtle.right(60)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)
turtle.right(90)
turtle.forward(200)

turtle.mainloop()


